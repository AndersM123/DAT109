package terningspill;

public class Terning {

	private int id;
	private int verdi;

	public Terning(int id, int verdi) {
		this.id = id;
		this.verdi = verdi;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getVerdi() {
		return verdi;
	}

	public void setVerdi(int verdi) {
		this.verdi = verdi;
	}

}
